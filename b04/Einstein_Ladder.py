#爱因斯坦曾经提出过这样一道有趣的数学题：有一个长阶梯，若每步上2阶，最后剩下1阶；若每步上3阶，最后剩2阶；若每步上5阶，最后剩下4阶；若每步上6阶，最后剩5阶；只有每步上7阶，最后刚好一阶也不剩。请问该阶梯至少有多少阶

num = 1

while num:
    if (num % 2 == 1) \
            and (num % 3 == 2)\
            and (num % 5 == 4) \
            and (num % 6 == 5) \
            and (num % 7 == 0):
        print(num)
        break
    else:
        num = num + 1
